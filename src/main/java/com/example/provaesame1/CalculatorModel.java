package com.example.provaesame1;

public class CalculatorModel {
    static float sum(float a, float b){
        return a+b;
    }

    static float sub(float a,float b){
        return a-b;
    }

    static float mul(float a,float b){
        return a*b;
    }

    static float div(float a,float b){
        return a/b;
    }
}
